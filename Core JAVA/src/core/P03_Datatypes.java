package core;

//two types
//1.Primitive -> byte,short,int,long,float,double,boolean,char
//2.Non - Primitive ->array,string,class
public class P03_Datatypes {
	public static void main(String[] args) {
		byte b = 1;
		System.out.println("value of b = " + b);// + -> concatination
		byte a = 2;
		System.out.println("value of a = " + a);
		System.out.println("addition of a and b = " + (a + b));
		short s = 12;
		System.out.println(s);
		int i = 234;
		System.out.println(i);
		long l = 234535;
		System.out.println(l);
		float f = 343.39086687457f;
		System.out.println(f);
		double d = 343.39086687457;
		System.out.println(d);
		boolean b1 = false;
		System.out.println(b1);
		char c = 'a';
		System.out.println(c);
	}
}
