package core;

//change value from one datatype to another datatype
//two types -> 1.implicit 2.explicit
public class TypesCasting {
	public static void main(String[] args) {
		double d = 3;
		System.out.println(d);
		int i  = (int)12.2;
		System.out.println(i);
		int a = 10, b = 3;
		double c = (double)a/b;
		System.out.println(c);
	}
}
